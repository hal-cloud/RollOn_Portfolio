// JudgeSystem から呼び出され、判定ロジックをここに集約する。
//=========================================================
#pragma once
#include <cmath>
#include "judge.h"

//=========================================================
// 判定ウィンドウ
//=========================================================

//-----------------------------------------------------
// JudgeWindow
// Perfect / Good / Miss それぞれの許容時間差（秒）を保持する
// 値が小さいほど厳しい判定になる
//-----------------------------------------------------
struct JudgeWindow {
    float perfect; // Perfect 判定の許容時間差（秒）
    float good;    // Good    判定の許容時間差（秒）
    float miss;    // Miss    判定の許容時間差（秒）
};

//=========================================================
// 判定関数
//=========================================================

//-----------------------------------------------------
// JudgeByTimeDiff
// ノーツのヒット時刻との時間差（秒）を受け取り、判定結果を返す
// 絶対値で比較するため、早押し・遅押しを区別しない
// Miss ウィンドウを超えた場合も Miss として扱う
//-----------------------------------------------------
inline JudgeType JudgeByTimeDiff(float diff, const JudgeWindow& w) {
    float ad = std::fabs(diff);
    if (ad <= w.perfect) return JudgeType::Perfect;
    if (ad <= w.good)    return JudgeType::Good;
    if (ad <= w.miss)    return JudgeType::Miss;
    return JudgeType::Miss;
}